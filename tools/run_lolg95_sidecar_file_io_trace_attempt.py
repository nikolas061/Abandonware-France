def read_csv(path: Path, delimiter: str = ",") -> list[dict[str, str]]:
    if not path.exists():
        return []
    with path.open(newline="", encoding="utf-8") as handle:
        return list(csv.DictReader(handle, delimiter=delimiter))


def write_csv(path: Path, fieldnames: list[str], rows: list[dict[str, str]], delimiter: str = ",") -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", newline="", encoding="utf-8") as handle:
        writer = csv.DictWriter(handle, fieldnames=fieldnames, delimiter=delimiter)
        writer.writeheader()
        writer.writerows(rows)


def parse_hex(text: str) -> int:
    value = text.strip().lower()
    if value.startswith("0x"):
        return int(value, 16)
    return int(value, 16)


def hex32(value: int) -> str:
    return f"0x{value & 0xFFFFFFFF:08x}"


def value_to_int(value: str) -> int:
    text = str(value).strip()
    if not text:
        return 0
    if text.lower().startswith("0x"):
        return int(text, 16)
    return int(text)


def load_targets(path: Path) -> list[dict[str, str]]:
    rows = read_csv(path)
    rows.sort(key=lambda row: value_to_int(row.get("expected_file_offset_start", "0")))
    return rows


def target_for_offset(targets: list[dict[str, str]], offset: int) -> dict[str, str]:
    for row in targets:
        start = value_to_int(row.get("expected_file_offset_start", "0"))
        end = value_to_int(row.get("expected_file_offset_end", "0"))
        if start <= offset < end:
            return row
    return {}


def build_command_file(path: Path, attach_pid: str, tracepoints: list[dict[str, str]], capture_stops: int) -> None:
    lines = [
        "# Generated by tools/run_lolg95_sidecar_file_io_trace_attempt.py",
        f"attach 0x{attach_pid}",
    ]
    for row in tracepoints:
        lines.append(f"break *{row['breakpoint_va']}")
    for _index in range(capture_stops):
        lines.extend(
            [
                "cont",
                "info regs",
                "x /x $ebx",
                "x /x ($ebx + 0x10)",
                "x /x ($ebx + 0x18)",
                "x /x $edi",
                "x /x $esi",
                "x /x $esp",
                "x /x ($esp + 4)",
                "x /x ($esp + 8)",
                "x /x ($esp + 12)",
                "x /x ($esp + 16)",
                "x /s *($ebx + 0x18)",
                "x /s *(int*)($ebx + 0x18)",
                "x /s *((int*)($ebx + 0x18))",
                "x /s *(char**)($ebx + 0x18)",
                "x /s *((char**)($ebx + 0x18))",
            ]
        )
    lines.append("quit")
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")


def parse_registers(block: str) -> dict[str, str]:
    registers: dict[str, str] = {}
    for source, target in {
        "EIP": "eip",
        "EBX": "ebx",
        "EDI": "edi",
        "ESI": "esi",
        "ESP": "esp",
    }.items():
        match = re.search(rf"\b{source}:([0-9a-fA-F]{{8,16}})\b", block)
        if match:
            registers[target] = hex32(parse_hex(match.group(1)))
    return registers


def parse_path_hint(block: str) -> str:
    for line in block.splitlines():
        if ".MIX" not in line.upper():
            continue
        text = line.strip()
        quoted = re.search(r'"([^"]*\.MIX[^"]*)"', text, flags=re.IGNORECASE)
        if quoted:
            return quoted.group(1)
        if ":" in text:
            text = text.split(":", 1)[1].strip()
        return text.strip().strip('"')
    return ""


def isolated_values(block: str) -> list[str]:
    return [hex32(parse_hex(value)) for value in re.findall(r"^\s*([0-9a-fA-F]{8})\s*$", block, re.MULTILINE)]


def sidecar_path(path: str) -> bool:
    return "l20_bbi_hd.mix" in path.replace("\\", "/").lower()


def parse_attach_pid(info_proc_text: str, executable_name: str) -> tuple[str, str]:
    direct_pid = parse_lolg95_pid(info_proc_text, executable_name)
    if direct_pid:
        return direct_pid, "runtime_executable"

    explorer_pids = []
    for line in info_proc_text.splitlines():
        match = re.search(r"^\s*=?([0-9a-fA-F]{8})\s+\d+\s+.*'explorer\.exe'", line, re.IGNORECASE)
        if match:
            explorer_pids.append(match.group(1).lower())
    if explorer_pids:
        return explorer_pids[-1], "explorer_fallback"
    return "", ""


def runtime_command_path(runtime_executable: Path, cwd: Path) -> str:
    return str(runtime_executable.resolve())


def normalize_pointer(value: str) -> str:
    try:
        return hex32(value_to_int(value))
    except ValueError:
        return ""


def build_archive_pointer_map(archive_rows: list[dict[str, str]]) -> dict[str, dict[str, str]]:
    pointer_map: dict[str, dict[str, str]] = {}
    for row in archive_rows:
        name = row.get("name", "")
        if not name:
            continue
        for field, role in [
            ("node", "node"),
            ("name_pointer", "name_pointer"),
            ("table_pointer", "table_pointer"),
            ("body_pointer", "body_pointer"),
        ]:
            pointer = normalize_pointer(row.get(field, ""))
            if pointer and pointer != "0x00000000":
                pointer_map[pointer] = {"archive_name": name, "archive_pointer_role": role}
    return pointer_map


def classify_rows(rows: list[dict[str, str]], targets: list[dict[str, str]]) -> None:
    last_sidecar_target: dict[str, str] = {}
    for row in rows:
        path_is_sidecar = sidecar_path(row["path"]) or sidecar_path(row.get("archive_name", ""))
        if row["kind"] == "SetFilePointer":
            offset = value_to_int(row["seek_offset"])
            target = target_for_offset(targets, offset)
            if path_is_sidecar and target:
                row["target_status"] = "sidecar_target_seek"
                row["target_file_id"] = target.get("file_id", "")
                row["target_index"] = target.get("index", "")
                row["target_range_start"] = target.get("expected_file_offset_start", "")
                row["target_range_end"] = target.get("expected_file_offset_end", "")
                last_sidecar_target = target
            elif target and not row["path"]:
                row["target_status"] = "target_offset_seek_unmapped_handle"
                row["target_file_id"] = target.get("file_id", "")
                row["target_index"] = target.get("index", "")
                row["target_range_start"] = target.get("expected_file_offset_start", "")
                row["target_range_end"] = target.get("expected_file_offset_end", "")
                row["notes"] = "offset matches a sidecar target range, but the archive path was not decoded"
            elif target:
                row["target_status"] = "non_sidecar_target_offset_seek"
                row["target_file_id"] = target.get("file_id", "")
                row["target_index"] = target.get("index", "")
                row["target_range_start"] = target.get("expected_file_offset_start", "")
                row["target_range_end"] = target.get("expected_file_offset_end", "")
                row["notes"] = "offset matches a target range on a non-sidecar path"
            elif path_is_sidecar:
                row["target_status"] = "sidecar_non_target_seek"
            else:
                row["target_status"] = "non_sidecar_seek"
        elif row["kind"] == "ReadFile":
            if path_is_sidecar and last_sidecar_target:
                row["target_status"] = "sidecar_target_read_after_seek"
                row["target_file_id"] = last_sidecar_target.get("file_id", "")
                row["target_index"] = last_sidecar_target.get("index", "")
                row["target_range_start"] = last_sidecar_target.get("expected_file_offset_start", "")
                row["target_range_end"] = last_sidecar_target.get("expected_file_offset_end", "")
            elif path_is_sidecar:
                row["target_status"] = "sidecar_read_without_target_seek"
            else:
                row["target_status"] = "non_sidecar_read"


def extract_trace_rows(
    raw_text: str,
    tracepoints: list[dict[str, str]],
    targets: list[dict[str, str]],
    archive_pointer_map: dict[str, dict[str, str]],
) -> tuple[list[dict[str, str]], int]:
    tracepoint_by_va = {hex32(value_to_int(row["breakpoint_va"])): row for row in tracepoints}
    stop_re = re.compile(r"Stopped on breakpoint\s+\d+\s+at\s+(0x[0-9a-fA-F]+)", re.IGNORECASE)
    matches = list(stop_re.finditer(raw_text))
    rows: list[dict[str, str]] = []
    breakpoint_hits = 0
    for index, match in enumerate(matches):
        breakpoint_va = hex32(parse_hex(match.group(1)))
        tracepoint = tracepoint_by_va.get(breakpoint_va)
        block_end = matches[index + 1].start() if index + 1 < len(matches) else len(raw_text)
        block = raw_text[match.start() : block_end]
        if not tracepoint:
            continue
        breakpoint_hits += 1
        registers = parse_registers(block)
        values = isolated_values(block)
        file_object = registers.get("ebx", "")
        file_object_word = values[0] if len(values) > 0 else ""
        file_handle = values[1] if len(values) > 1 else ""
        path_pointer = values[2] if len(values) > 2 else ""
        archive_pointer = archive_pointer_map.get(normalize_pointer(path_pointer), {})
        edi = values[3] if len(values) > 3 else registers.get("edi", "")
        esi = values[4] if len(values) > 4 else registers.get("esi", "")
        esp0 = values[5] if len(values) > 5 else ""
        esp4 = values[6] if len(values) > 6 else ""
        esp8 = values[7] if len(values) > 7 else ""
        esp12 = values[8] if len(values) > 8 else ""
        esp16 = values[9] if len(values) > 9 else ""
        kind = tracepoint.get("kind", "")
        rows.append(
            {
                "row_index": str(len(rows)),
                "breakpoint_va": breakpoint_va,
                "kind": kind,
                "path": parse_path_hint(block) or archive_pointer.get("archive_name", ""),
                "path_pointer": path_pointer,
                "archive_pointer_role": archive_pointer.get("archive_pointer_role", ""),
                "archive_name": archive_pointer.get("archive_name", ""),
                "file_object": file_object,
                "file_object_word": file_object_word,
                "file_handle": file_handle,
                "edi": edi,
                "esi": esi,
                "esp0": esp0,
                "esp4": esp4,
                "esp8": esp8,
                "esp12": esp12,
                "esp16": esp16,
                "seek_offset": edi if kind == "SetFilePointer" else "",
                "seek_method": esi if kind == "SetFilePointer" else "",
                "read_size": esp8 if kind == "ReadFile" else "",
                "target_status": "",
                "target_file_id": "",
                "target_index": "",
                "target_range_start": "",
                "target_range_end": "",
                "notes": "",
            }
        )
    classify_rows(rows, targets)
    return rows, breakpoint_hits


def render_table(rows: list[dict[str, str]], fields: list[str]) -> str:
    head = "".join(f"<th>{html.escape(field)}</th>" for field in fields)
    body = []
    for row in rows:
        body.append("<tr>" + "".join(f"<td>{html.escape(row.get(field, ''))}</td>" for field in fields) + "</tr>")
    return f"<table><thead><tr>{head}</tr></thead><tbody>{''.join(body)}</tbody></table>"


