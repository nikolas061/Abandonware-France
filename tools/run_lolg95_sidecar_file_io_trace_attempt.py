def run_attempt(args: argparse.Namespace) -> dict[str, str]:
    output = args.output.resolve()
    output.mkdir(parents=True, exist_ok=True)
    info_proc_log = output / "info_proc.log"
    raw_log = output / "raw.log"
    trace_path = output / "trace.tsv"
    archives_path = output / "archives.tsv"
    archive_targets_path = output / "archive_targets.tsv"
    command_file = output / "winedbg_file_io_commands.txt"
    xdotool_log = output / "xdotool.log"
    pilot_key_log = output / "pilot_keys.log"
    ps_log = output / "ps.txt"
    force_level_log = output / "force_level_write.log"
    game_stdout = output / "game.out"
    game_stderr = output / "game.err"

    tracepoints = read_csv(args.tracepoints, delimiter="\t")
    targets = load_targets(args.targets)
    expected = load_expected(args.expected_ids)
    issues: list[str] = []
    if not tracepoints:
        issues.append("missing_tracepoints")
    if not targets:
        issues.append("missing_targets")
    if not expected:
        issues.append("missing_expected_ids")

    wine = args.wine or shutil.which("wine") or ""
    winedbg = args.winedbg or shutil.which("winedbg") or ""
    xdotool = args.xdotool or shutil.which("xdotool") or ""
    if not wine:
        issues.append("missing_wine")
    if not winedbg:
        issues.append("missing_winedbg")
    if not args.runtime_executable.is_file():
        issues.append("missing_runtime_executable")
    if args.click and not xdotool:
        issues.append("missing_xdotool")

    env = os.environ.copy()
    env["WINEPREFIX"] = str(args.wineprefix.resolve())
    env["WINEDEBUG"] = args.winedebug
    if args.xdotool_library_path:
        env["LD_LIBRARY_PATH"] = (
            args.xdotool_library_path
            if not env.get("LD_LIBRARY_PATH")
            else f"{args.xdotool_library_path}:{env['LD_LIBRARY_PATH']}"
        )

    runtime_args = args.runtime_arg or ["-CD", r"D:\WESTWOOD\LOLG"]
    runtime_command = args.runtime_command or runtime_command_path(args.runtime_executable, args.cwd)
    if args.direct:
        game_command = [wine or "wine", runtime_command, *runtime_args]
    else:
        game_command = [
            wine or "wine",
            "explorer",
            f"/desktop={args.wine_desktop}",
            runtime_command,
            *runtime_args,
        ]
    session_status = "dry_run" if args.dry_run else "not_started"
    attach_pid = ""
    attach_pid_source = ""
    linux_pid = ""
    force_level_write_status = ""
    xdotool_status = ""
    pilot_key_status = ""
    winedbg_returncode = ""
    winedbg_timed_out = "0"
    game_process: subprocess.Popen[str] | None = None
    archive_rows: list[dict[str, str]] = []
    archive_target_rows: list[dict[str, str]] = []
    archive_scan_phase = ""

    def refresh_archive_scan(phase: str) -> None:
        nonlocal archive_rows, archive_target_rows, archive_scan_phase
        if not linux_pid:
            issues.append(f"missing_runtime_memory_probe_pid:{phase}")
            return
        rows, target_rows, scan_issues = scan_archive_list(linux_pid, expected, args.max_archives)
        if rows:
            archive_rows = rows
            archive_target_rows = target_rows
            archive_scan_phase = phase
        issues.extend(f"archive_scan:{phase}:{issue}" for issue in scan_issues)
        write_csv(archives_path, ARCHIVE_FIELDS, archive_rows, delimiter="\t")
        write_csv(archive_targets_path, ARCHIVE_TARGET_FIELDS, archive_target_rows, delimiter="\t")

    if args.dry_run:
        build_command_file(command_file, "00000000", tracepoints, args.capture_stops)
        raw_log.write_text("", encoding="utf-8")
        info_proc_log.write_text("", encoding="utf-8")
        write_csv(archives_path, ARCHIVE_FIELDS, archive_rows, delimiter="\t")
        write_csv(archive_targets_path, ARCHIVE_TARGET_FIELDS, archive_target_rows, delimiter="\t")
        if args.force_level_index is not None or args.force_level_slot is not None:
            planned_writes = []
            if args.force_level_index is not None:
                planned_writes.append(f"0x005b0948={args.force_level_index}")
            if args.force_level_slot is not None:
                planned_writes.append(f"0x005b094c={args.force_level_slot}")
            force_level_write_status = "dry_run"
            force_level_log.write_text("dry_run " + " ".join(planned_writes) + "\n", encoding="utf-8")
        else:
            force_level_log.write_text("", encoding="utf-8")
    elif any(issue.startswith("missing_") for issue in issues):
        raw_log.write_text("", encoding="utf-8")
        info_proc_log.write_text("", encoding="utf-8")
        force_level_log.write_text("", encoding="utf-8")
        write_csv(archives_path, ARCHIVE_FIELDS, archive_rows, delimiter="\t")
        write_csv(archive_targets_path, ARCHIVE_TARGET_FIELDS, archive_target_rows, delimiter="\t")
    else:
        try:
            with game_stdout.open("w", encoding="utf-8", errors="replace") as stdout_handle, game_stderr.open(
                "w", encoding="utf-8", errors="replace"
            ) as stderr_handle:
                game_process = subprocess.Popen(
                    game_command,
                    stdout=stdout_handle,
                    stderr=stderr_handle,
                    text=True,
                    env=env,
                    cwd=args.cwd,
                )
            time.sleep(args.startup_wait)
            info_stdout, info_stderr, _info_returncode, _info_timeout = run_with_timeout(
                [winedbg or "winedbg", "--command", "info proc"],
                args.info_timeout,
                env,
                args.cwd,
            )
            info_proc_text = info_stdout + info_stderr
            info_proc_log.write_text(info_proc_text, encoding="utf-8", errors="replace")
            attach_pid, attach_pid_source = parse_attach_pid(info_proc_text, args.runtime_executable.name)

            if args.force_level_index is not None or args.force_level_slot is not None:
                ps_stdout, ps_stderr, _ps_returncode, _ps_timeout = run_with_timeout(
                    ["ps", "-eo", "pid=,comm=,args="],
                    8,
                    env,
                    args.cwd,
                )
                ps_text = ps_stdout + ps_stderr
                ps_log.write_text(ps_text, encoding="utf-8", errors="replace")
                linux_pid = parse_lolg95_linux_pid(ps_text, args.runtime_executable.name)
                if not linux_pid:
                    force_level_write_status = "missing_linux_pid"
                    issues.append("missing_lolg95_linux_process")
                    force_level_log.write_text("", encoding="utf-8")
                else:
                    writes = []
                    if args.force_level_index is not None:
                        writes.append((0x005B0948, args.force_level_index))
                    if args.force_level_slot is not None:
                        writes.append((0x005B094C, args.force_level_slot))
                    force_log_text, force_issues = write_process_dwords(linux_pid, writes)
                    force_level_log.write_text(force_log_text, encoding="utf-8", errors="replace")
                    if force_issues:
                        force_level_write_status = "failed"
                        issues.extend(force_issues)
                    else:
                        force_level_write_status = "pass"

            refresh_archive_scan("pre_debug")

            if not attach_pid:
                issues.append("missing_lolg95_process")
                raw_log.write_text("", encoding="utf-8")
            else:
                build_command_file(command_file, attach_pid, tracepoints, args.capture_stops)
                winedbg_command = [winedbg or "winedbg", "--file", str(command_file)]
                with raw_log.open("w", encoding="utf-8", errors="replace") as raw_handle:
                    debugger = subprocess.Popen(
                        winedbg_command,
                        stdout=raw_handle,
                        stderr=subprocess.STDOUT,
                        text=True,
                        env=env,
                        cwd=args.cwd,
                    )
                    time.sleep(args.click_delay)
                    if args.click:
                        click_command = [
                            xdotool or "xdotool",
                            "search",
                            "--name",
                            args.click_window_name,
                            "mousemove",
                            "--window",
                            "%@",
                            str(args.click_x),
                            str(args.click_y),
                            "click",
                            "1",
                        ]
                        click_results = []
                        for _index in range(args.click_count):
                            click_stdout, click_stderr, click_returncode, click_timeout = run_with_timeout(
                                click_command,
                                args.click_timeout,
                                env,
                                args.cwd,
                            )
                            click_results.append(
                                f"returncode={click_returncode or 'timeout'} timeout={click_timeout}\n"
                                f"{click_stdout}{click_stderr}"
                            )
                            if args.click_interval:
                                time.sleep(args.click_interval)
                        xdotool_log.write_text("\n--- click ---\n".join(click_results), encoding="utf-8")
                        xdotool_status = ";".join(line.splitlines()[0] for line in click_results)
                    if args.pilot_key and xdotool:
                        key_results = []
                        for key_name in args.pilot_key:
                            if args.pilot_key_delay:
                                time.sleep(args.pilot_key_delay)
                            key_command = [
                                xdotool,
                                "search",
                                "--name",
                                args.click_window_name,
                                "key",
                                "--window",
                                "%@",
                                key_name,
                            ]
                            key_stdout, key_stderr, key_returncode, key_timeout = run_with_timeout(
                                key_command,
                                args.click_timeout,
                                env,
                                args.cwd,
                            )
                            key_results.append(
                                f"key={key_name} returncode={key_returncode or 'timeout'} timeout={key_timeout}\n"
                                f"{key_stdout}{key_stderr}"
                            )
                        pilot_key_log.write_text("\n--- key ---\n".join(key_results), encoding="utf-8")
                        pilot_key_status = ";".join(line.splitlines()[0] for line in key_results)
                    try:
                        winedbg_returncode = str(debugger.wait(timeout=args.attach_timeout))
                        session_status = "exited_0" if winedbg_returncode == "0" else f"exited_{winedbg_returncode}"
                    except subprocess.TimeoutExpired:
                        winedbg_timed_out = "1"
                        session_status = "started_timeout"
                        debugger.kill()
                        debugger.wait(timeout=8)
                if args.post_debug_archive_wait:
                    time.sleep(args.post_debug_archive_wait)
                refresh_archive_scan("post_debug")
        finally:
            stop_process(game_process)

    raw_text = raw_log.read_text(encoding="utf-8", errors="replace") if raw_log.exists() else ""
    archive_pointer_map = build_archive_pointer_map(archive_rows)
    trace_rows, breakpoint_hits = extract_trace_rows(raw_text, tracepoints, targets, archive_pointer_map)
    write_csv(trace_path, TRACE_FIELDS, trace_rows, delimiter="\t")
    if breakpoint_hits == 0 and not args.dry_run:
        issues.append("no_file_io_breakpoints")
    if not trace_rows and not args.dry_run:
        issues.append("no_file_io_trace_rows")

    setfilepointer_rows = [row for row in trace_rows if row["kind"] == "SetFilePointer"]
    readfile_rows = [row for row in trace_rows if row["kind"] == "ReadFile"]
    sidecar_path_rows = [row for row in trace_rows if sidecar_path(row["path"])]
    archive_pointer_mapped_rows = [row for row in trace_rows if row["archive_name"]]
    sidecar_archive_pointer_rows = [row for row in archive_pointer_mapped_rows if sidecar_path(row["archive_name"])]
    target_offset_seek_rows = [
        row
        for row in trace_rows
        if row["target_status"]
        in {"sidecar_target_seek", "target_offset_seek_unmapped_handle", "non_sidecar_target_offset_seek"}
    ]
    unmapped_target_offset_seek_rows = [
        row for row in trace_rows if row["target_status"] == "target_offset_seek_unmapped_handle"
    ]
    target_seek_rows = [row for row in trace_rows if row["target_status"] == "sidecar_target_seek"]
    target_read_rows = [row for row in trace_rows if row["target_status"] == "sidecar_target_read_after_seek"]
    target_ids_seen = sorted({row["target_file_id"] for row in target_seek_rows if row["target_file_id"]})
    target_read_ids_seen = sorted({row["target_file_id"] for row in target_read_rows if row["target_file_id"]})
    runtime_sidecar_first = [
        row["file_id"] for row in archive_target_rows if row.get("first_status") == "sidecar"
    ]
    runtime_base_first = [row["file_id"] for row in archive_target_rows if row.get("first_status") == "base"]
    runtime_missing = [row["file_id"] for row in archive_target_rows if row.get("first_status") == "missing"]
    runtime_unknown_first = [
        row["file_id"] for row in archive_target_rows if row.get("first_status") == "unknown_size"
    ]

    if target_read_rows:
        status = "pass"
        next_step = "confirm visual output for the read VQA and promote the sidecar runtime path"
    elif target_seek_rows:
        status = "gap"
        next_step = "extend capture until ReadFile follows the observed target sidecar seek"
    elif runtime_base_first:
        status = "gap"
        next_step = "change sidecar insertion order so duplicate VQA IDs are found before l20_bbI.MIX"
    elif sidecar_path_rows:
        status = "gap"
        next_step = "drive gameplay until a sidecar file seek falls inside one VQA target range"
    else:
        status = "gap"
        next_step = "drive gameplay or targeted VQA request until l20_bbI_HD.MIX file I/O is observed"

    command_file_text = str(command_file)
    summary = {
        "status": status,
        "session_status": session_status,
        "tracepoints": str(len(tracepoints)),
        "targets": str(len(targets)),
        "runtime_executable": str(args.runtime_executable),
        "runtime_args": shlex.join(runtime_args),
        "wineprefix": str(args.wineprefix),
        "wine_desktop": args.wine_desktop,
        "startup_wait_seconds": str(args.startup_wait),
        "attach_pid": attach_pid,
        "attach_pid_source": attach_pid_source,
        "linux_pid": linux_pid,
        "force_level_index": "" if args.force_level_index is None else str(args.force_level_index),
        "force_level_slot": "" if args.force_level_slot is None else str(args.force_level_slot),
        "force_level_write_status": force_level_write_status,
        "force_level_write_log": str(force_level_log) if force_level_write_status else "",
        "command": shlex.join(game_command),
        "winedbg_command": shlex.join([winedbg or "winedbg", "--file", command_file_text]),
        "winedbg_returncode": winedbg_returncode,
        "winedbg_timed_out": winedbg_timed_out,
        "xdotool_command": (
            shlex.join(
                [
                    xdotool or "xdotool",
                    "search",
                    "--name",
                    args.click_window_name,
                    "mousemove",
                    "--window",
                    "%@",
                    str(args.click_x),
                    str(args.click_y),
                    "click",
                    "1",
                ]
            )
            if args.click
            else ""
        ),
        "xdotool_status": xdotool_status,
        "pilot_keys": " ".join(args.pilot_key),
        "pilot_key_status": pilot_key_status,
        "command_file": command_file_text,
        "info_proc_log": str(info_proc_log),
        "raw_log": str(raw_log),
        "trace": str(trace_path),
        "archives": str(archives_path),
        "archive_targets": str(archive_targets_path),
        "archive_scan_phase": archive_scan_phase,
        "capture_stops": str(args.capture_stops),
        "archive_nodes": str(len(archive_rows)),
        "archive_names": ",".join(row["name"] for row in archive_rows if row.get("name")),
        "runtime_sidecar_first": ",".join(runtime_sidecar_first),
        "runtime_base_first": ",".join(runtime_base_first),
        "runtime_missing": ",".join(runtime_missing),
        "runtime_unknown_first": ",".join(runtime_unknown_first),
        "breakpoint_hits": str(breakpoint_hits),
        "setfilepointer_hits": str(len(setfilepointer_rows)),
        "readfile_hits": str(len(readfile_rows)),
        "sidecar_path_hits": str(len(sidecar_path_rows)),
        "archive_pointer_mapped_hits": str(len(archive_pointer_mapped_rows)),
        "sidecar_archive_pointer_hits": str(len(sidecar_archive_pointer_rows)),
        "target_offset_seek_hits": str(len(target_offset_seek_rows)),
        "unmapped_target_offset_seek_hits": str(len(unmapped_target_offset_seek_rows)),
        "target_seek_hits": str(len(target_seek_rows)),
        "target_read_hits": str(len(target_read_rows)),
        "target_ids_seen": ",".join(target_ids_seen),
        "target_read_ids_seen": ",".join(target_read_ids_seen),
        "issues": ";".join(dict.fromkeys(issues)),
        "next_step": next_step,
    }
    write_csv(output / "summary.csv", SUMMARY_FIELDS, [summary])
    write_html(output / "index.html", summary, trace_rows)
    return summary


