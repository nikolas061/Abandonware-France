def read_csv(path: Path, delimiter: str = ",") -> list[dict[str, str]]:
    if not path.exists():
        return []
    with path.open(newline="", encoding="utf-8") as handle:
        return list(csv.DictReader(handle, delimiter=delimiter))


def write_csv(path: Path, fieldnames: list[str], rows: list[dict[str, str]], delimiter: str = ",") -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", newline="", encoding="utf-8") as handle:
        writer = csv.DictWriter(handle, fieldnames=fieldnames, delimiter=delimiter)
        writer.writeheader()
        writer.writerows(rows)


def compact_text(value: str | bytes | None) -> str:
    if value is None:
        return ""
    if isinstance(value, bytes):
        return value.decode("utf-8", errors="replace")
    return value


def normalize_hex(text: str) -> str:
    value = text.strip().lower()
    if not value:
        return ""
    if value.startswith("0x"):
        return f"0x{int(value, 16) & 0xFFFFFFFF:08x}"
    return f"0x{int(value, 16) & 0xFFFFFFFF:08x}"


def build_command_file(path: Path, tracepoints: list[dict[str, str]], capture_stops: int) -> None:
    lines = [
        "# Generated by tools/run_lolg95_winedbg_loader_trace_attempt.py",
        "# Internal winedbg syntax. Breakpoints are sourced from the loader trace contract.",
    ]
    for row in tracepoints:
        lines.append(f"break *{row['breakpoint_va']}")
    for _index in range(capture_stops):
        lines.extend(
            [
                "cont",
                "info regs",
                "info stack",
                "x /x $esp",
                "x /x ($esp + 4)",
                "x /x ($esp + 8)",
                "x /x ($esp + 12)",
                "x /x ($esp + 16)",
                "x /x ($esp + 20)",
                "x /x ($esp + 24)",
                "x /s $eax",
                "x /s $ecx",
                "x /s $edx",
                "x /s $esi",
                "x /s *$esp",
            ]
        )
    lines.append("quit")
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")


def parse_display_value(line: str) -> tuple[str, str] | None:
    checks = [
        ("$eip", "eip"),
        ("$eax", "eax"),
        ("$ecx", "ecx"),
        ("$edx", "edx"),
        ("$esi", "esi"),
        ("$edi", "edi"),
        ("$esp + 24", "esp24"),
        ("$esp+24", "esp24"),
        ("$esp + 20", "esp20"),
        ("$esp+20", "esp20"),
        ("$esp + 16", "esp16"),
        ("$esp+16", "esp16"),
        ("$esp + 12", "esp12"),
        ("$esp+12", "esp12"),
        ("$esp + 8", "esp8"),
        ("$esp+8", "esp8"),
        ("$esp + 4", "esp4"),
        ("$esp+4", "esp4"),
        ("*$esp", "esp0"),
        ("*($esp)", "esp0"),
    ]
    key = ""
    for marker, candidate in checks:
        if marker in line:
            key = candidate
            break
    if not key:
        return None
    match = re.search(r"(0x[0-9a-fA-F]+|[0-9a-fA-F]{8,16})\s*$", line.strip())
    if not match:
        match = re.search(r"=\s*(0x[0-9a-fA-F]+|[0-9a-fA-F]{8,16})", line)
    if not match:
        return None
    return key, normalize_hex(match.group(1))


def parse_register_values(line: str) -> dict[str, str]:
    values: dict[str, str] = {}
    for source, target in {
        "EIP": "eip",
        "EAX": "eax",
        "ECX": "ecx",
        "EDX": "edx",
        "ESI": "esi",
        "EDI": "edi",
        "ESP": "esp",
    }.items():
        match = re.search(rf"\b{source}:([0-9a-fA-F]{{8,16}})\b", line)
        if match:
            values[target] = normalize_hex(match.group(1))
    return values


def parse_stack_words(line: str) -> list[tuple[str, str]]:
    match = re.search(r"(0x[0-9a-fA-F]+|[0-9a-fA-F]{8,16})\s*:\s*((?:[0-9a-fA-F]{8,16}\s*)+)$", line)
    if not match:
        return []
    base = int(normalize_hex(match.group(1)), 16)
    values = re.findall(r"[0-9a-fA-F]{8,16}", match.group(2))
    return [(f"0x{(base + index * 4) & 0xFFFFFFFF:08x}", normalize_hex(value)) for index, value in enumerate(values)]


def parse_memory_string(line: str) -> tuple[str, str] | None:
    match = re.search(r"(0x[0-9a-fA-F]+|[0-9a-fA-F]{8,16})\s*:\s*\"([^\"]*)\"", line)
    if not match:
        return None
    return normalize_hex(match.group(1)), match.group(2)


def parse_bare_path_string(line: str) -> str:
    value = line.strip()
    if not value or "." not in value:
        return ""
    if re.fullmatch(r"[A-Za-z0-9_./:\\-]+", value):
        return value
    return ""


def assign_stack_word(current: dict[str, str], address: str, value: str) -> bool:
    esp = current.get("esp")
    if not esp:
        return False
    slot = (int(address, 16) - int(esp, 16)) & 0xFFFFFFFF
    if slot in {0, 4, 8, 12, 16, 20, 24}:
        current[f"esp{slot}"] = value
        return True
    return False


def record_path_string(current: dict[str, str], pointer: str, text: str) -> None:
    if current.get("path_ascii"):
        return
    current["path_pointer"] = pointer
    current["path_ascii"] = text


def preferred_path_pointer(current: dict[str, str]) -> str:
    for key in ("esp0", "eax", "edx", "ecx", "esi"):
        value = current.get(key, "")
        if value and int(value, 16) >= 0x10000:
            return value
    return current.get("esp0") or current.get("esi", "")


def extract_trace_rows(raw_log: str, tracepoints: list[dict[str, str]]) -> tuple[list[dict[str, str]], int, int, int]:
    by_breakpoint = {normalize_hex(row["breakpoint_va"]): row for row in tracepoints}
    stop_re = re.compile(r"Stopped on breakpoint\s+\d+\s+at\s+(0x[0-9a-fA-F]+)", re.IGNORECASE)
    invalid_re = re.compile(r"Invalid address\s+\((0x[0-9a-fA-F]+)", re.IGNORECASE)
    rows: list[dict[str, str]] = []
    breakpoint_hits = 0
    invalid_breakpoints = 0
    incomplete_hits = 0
    current: dict[str, str] | None = None

    def flush_current() -> None:
        nonlocal current, incomplete_hits
        if not current:
            return
        required = ("eip", "eax", "ecx", "edx", "esi", "edi")
        complete = all(current.get(field) for field in required)
        if not complete:
            incomplete_hits += 1
        contract = by_breakpoint[current["breakpoint_va"]]
        rows.append(
            {
                "tracepoint_id": contract["tracepoint_id"],
                "breakpoint_va": current["breakpoint_va"],
                "kind": contract["kind"],
                "target": contract["target"],
                "path_pointer": current.get("path_pointer", ""),
                "path_ascii": current.get("path_ascii", ""),
                "eip": current.get("eip", ""),
                "eax": current.get("eax", ""),
                "ecx": current.get("ecx", ""),
                "edx": current.get("edx", ""),
                "esi": current.get("esi", ""),
                "edi": current.get("edi", ""),
                "esp0": current.get("esp0", ""),
                "esp4": current.get("esp4", ""),
                "esp8": current.get("esp8", ""),
                "esp12": current.get("esp12", ""),
                "esp16": current.get("esp16", ""),
                "esp20": current.get("esp20", ""),
                "esp24": current.get("esp24", ""),
                "capture_note": (
                    "winedbg loader trace capture with path string"
                    if current.get("path_ascii")
                    else (
                        "winedbg loader trace capture; path pointer still needs memory-string extraction"
                        if complete
                        else "partial winedbg breakpoint hit; register/stack display was not captured"
                    )
                ),
            }
        )
        current = None

    for line in raw_log.splitlines():
        invalid = invalid_re.search(line)
        if invalid and normalize_hex(invalid.group(1)) in by_breakpoint:
            invalid_breakpoints += 1
        stop = stop_re.search(line)
        if stop:
            flush_current()
            breakpoint_va = normalize_hex(stop.group(1))
            if breakpoint_va in by_breakpoint:
                breakpoint_hits += 1
                current = {"breakpoint_va": breakpoint_va, "eip": breakpoint_va}
            else:
                current = None
            continue
        parsed = parse_display_value(line)
        if parsed and current is not None:
            key, value = parsed
            current[key] = value
            continue
        registers = parse_register_values(line)
        if registers and current is not None:
            current.update(registers)
            continue
        memory_string = parse_memory_string(line)
        if memory_string and current is not None:
            pointer, text = memory_string
            record_path_string(current, pointer, text)
            continue
        bare_path = parse_bare_path_string(line)
        if bare_path and current is not None:
            record_path_string(current, preferred_path_pointer(current), bare_path)
            continue
        for address, value in parse_stack_words(line):
            if current is not None:
                assign_stack_word(current, address, value)
    flush_current()
    return rows, breakpoint_hits, invalid_breakpoints, incomplete_hits


def render_table(rows: list[dict[str, str]], fields: list[str]) -> str:
    head = "".join(f"<th>{html.escape(field)}</th>" for field in fields)
    body = []
    for row in rows:
        body.append("<tr>" + "".join(f"<td>{html.escape(row.get(field, ''))}</td>" for field in fields) + "</tr>")
    return f"<table><thead><tr>{head}</tr></thead><tbody>{''.join(body)}</tbody></table>"


