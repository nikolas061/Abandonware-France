def run_attempt(args: argparse.Namespace) -> tuple[dict[str, str], list[dict[str, str]]]:
    output = args.output
    output.mkdir(parents=True, exist_ok=True)
    command_file = output / "winedbg_commands.txt"
    raw_log = output / "raw.log"
    trace = output / "trace.tsv"
    tracepoints = read_csv(args.tracepoints, delimiter="\t")
    issues: list[str] = []
    if not tracepoints:
        issues.append("missing_tracepoints")
    else:
        build_command_file(command_file, tracepoints, args.capture_stops)

    xvfb_run = args.xvfb_run or shutil.which("xvfb-run") or ""
    winedbg = args.winedbg or shutil.which("winedbg") or ""
    wine = shutil.which("wine") or ""
    runtime_executable = args.runtime_executable
    wineprefix = args.wineprefix or (output / "wineprefix")
    if not xvfb_run:
        issues.append("missing_xvfb_run")
    if not winedbg:
        issues.append("missing_winedbg")
    if not runtime_executable.is_file():
        issues.append("missing_runtime_executable")

    command = [
        xvfb_run or "xvfb-run",
        "-a",
        "-s",
        args.screen,
        winedbg or "winedbg",
        "--file",
        str(command_file),
        str(runtime_executable),
    ]
    session_status = "dry_run" if args.dry_run else "not_started"
    returncode = ""
    timed_out = "0"
    stdout_text = ""
    stderr_text = ""
    renderer_setup_status = ""
    if args.dry_run:
        raw_log.write_text("", encoding="utf-8")
    elif not any(issue in issues for issue in ("missing_tracepoints", "missing_xvfb_run", "missing_winedbg", "missing_runtime_executable")):
        env = os.environ.copy()
        env["WINEPREFIX"] = str(wineprefix.resolve())
        if args.wine_renderer:
            if not wine:
                renderer_setup_status = "missing_wine"
                issues.append(renderer_setup_status)
            else:
                renderer = subprocess.run(
                    [
                        wine,
                        "reg",
                        "add",
                        r"HKCU\Software\Wine\Direct3D",
                        "/v",
                        "renderer",
                        "/d",
                        args.wine_renderer,
                        "/f",
                    ],
                    stdout=subprocess.PIPE,
                    stderr=subprocess.PIPE,
                    text=True,
                    errors="replace",
                    check=False,
                    timeout=30,
                    env=env,
                )
                renderer_setup_status = f"exited_{renderer.returncode}"
                if renderer.returncode != 0:
                    issues.append(f"renderer_setup_failed:{renderer.returncode}")
        try:
            result = subprocess.run(
                command,
                stdout=subprocess.PIPE,
                stderr=subprocess.PIPE,
                text=True,
                errors="replace",
                check=False,
                timeout=args.timeout,
                env=env,
            )
            returncode = str(result.returncode)
            session_status = "exited_0" if result.returncode == 0 else f"exited_{result.returncode}"
            stdout_text = result.stdout
            stderr_text = result.stderr
        except subprocess.TimeoutExpired as exc:
            timed_out = "1"
            session_status = "started_timeout"
            stdout_text = compact_text(exc.stdout)
            stderr_text = compact_text(exc.stderr)
        except OSError as exc:
            session_status = f"launch_error:{type(exc).__name__}"
            issues.append(session_status)
            stderr_text = str(exc)
        raw_log.write_text(stdout_text + stderr_text, encoding="utf-8", errors="replace")

    raw_text = raw_log.read_text(encoding="utf-8", errors="replace")
    trace_rows, breakpoint_hits, invalid_breakpoints, incomplete_hits = extract_trace_rows(raw_text, tracepoints)
    write_csv(trace, TRACE_FIELDS, trace_rows, delimiter="\t")
    if incomplete_hits:
        issues.append(f"incomplete_breakpoint_hits:{incomplete_hits}")
    if invalid_breakpoints:
        issues.append(f"invalid_breakpoints:{invalid_breakpoints}")
    if breakpoint_hits == 0 and not args.dry_run:
        issues.append("no_contract_breakpoint_hits")
    if not trace_rows and not args.dry_run:
        issues.append("no_trace_rows")

    createfile_hits = sum(1 for row in trace_rows if row.get("kind") == "createfile")
    mix_constructor_hits = sum(1 for row in trace_rows if row.get("kind") == "mix_constructor")
    captured_paths = [row.get("path_ascii", "") for row in trace_rows if row.get("path_ascii", "")]
    raw_upper = raw_text.upper()
    l20_bbi_mentions = raw_upper.count("L20_BBI")
    l20_bbi_hd_mentions = raw_upper.count("L20_BBI_HD")
    if l20_bbi_hd_mentions:
        status = "pass"
        next_step = "confirm that sidecar payload IDs are read from L20_BBI_HD.MIX"
    elif trace_rows and incomplete_hits:
        status = "gap"
        next_step = "adjust winedbg capture commands to record registers/stack at the hit breakpoint, then extract path strings"
    elif any(row.get("path_ascii") for row in trace_rows):
        status = "gap"
        next_step = "continue the loader trace beyond the captured startup file opens until L20_BBI.MIX is observed"
    elif trace_rows:
        status = "gap"
        next_step = "extract path strings from captured pointers and add the L20_BBI_HD.MIX fallback hook"
    else:
        status = "gap"
        next_step = "exercise startup/gameplay far enough to hit the loader tracepoints or adjust breakpoint timing"

    summary = {
        "status": status,
        "session_status": session_status,
        "tracepoints": str(args.tracepoints),
        "runtime_executable": str(runtime_executable),
        "timeout_seconds": str(args.timeout),
        "command": shlex.join(command),
        "returncode": returncode,
        "timed_out": timed_out,
        "xvfb_run": xvfb_run,
        "screen": args.screen,
        "winedbg": winedbg,
        "wine_renderer": args.wine_renderer,
        "renderer_setup_status": renderer_setup_status,
        "wineprefix": str(wineprefix),
        "command_file": str(command_file),
        "raw_log": str(raw_log),
        "trace": str(trace),
        "capture_stops": str(args.capture_stops),
        "contract_tracepoints": str(len(tracepoints)),
        "breakpoint_hits": str(breakpoint_hits),
        "invalid_breakpoints": str(invalid_breakpoints),
        "extracted_rows": str(len(trace_rows)),
        "createfile_hits": str(createfile_hits),
        "mix_constructor_hits": str(mix_constructor_hits),
        "path_rows": str(len(captured_paths)),
        "unique_paths": str(len({path.lower() for path in captured_paths})),
        "l20_bbi_mentions": str(l20_bbi_mentions),
        "l20_bbi_hd_mentions": str(l20_bbi_hd_mentions),
        "issues": ";".join(dict.fromkeys(issues)),
        "next_step": next_step,
    }
    return summary, trace_rows


def main() -> None:
    parser = argparse.ArgumentParser(description="Run a bounded winedbg loader trace attempt for LOLG95.EXE.")
    parser.add_argument("--tracepoints", type=Path, default=DEFAULT_TRACEPOINTS)
    parser.add_argument("-o", "--output", type=Path, default=DEFAULT_OUTPUT)
    parser.add_argument("--runtime-executable", type=Path, default=Path("LOLG95.EXE"))
    parser.add_argument("--xvfb-run", default="")
    parser.add_argument("--winedbg", default="")
    parser.add_argument("--wine-renderer", default="")
    parser.add_argument("--wineprefix", type=Path)
    parser.add_argument("--screen", default="-screen 0 640x480x16")
    parser.add_argument("--timeout", type=int, default=45)
    parser.add_argument("--capture-stops", type=int, default=256)
    parser.add_argument("--cont-count", type=int, default=2000, help=argparse.SUPPRESS)
    parser.add_argument("--dry-run", action="store_true")
    parser.add_argument("--fail-on-gaps", action="store_true")
    args = parser.parse_args()

    summary, trace_rows = run_attempt(args)
    args.output.mkdir(parents=True, exist_ok=True)
    write_csv(args.output / "summary.csv", SUMMARY_FIELDS, [summary])
    write_html(args.output / "index.html", summary, trace_rows)
    print(f"LOLG95 winedbg loader trace attempt: {summary['status']} ({summary['session_status']})")
    print(f"Summary: {args.output / 'summary.csv'}")
    print(f"Trace rows: {summary['extracted_rows']} / hits: {summary['breakpoint_hits']}")
    if args.fail_on_gaps and summary["status"] != "pass":
        raise SystemExit(1)


if __name__ == "__main__":
    main()
